    private void populateTable() {

     DefaultTableModel dtm=(DefaultTableModel) jStudentDetails.getModel();
     dtm.setNumRows(0);
     for(StudentClass StudentClass:StudentDirectory.getStudentList()){
         Object[] row=new Object[4];
         row[0]=StudentClass;
         row[1]=StudentClass.getLastName();
         row[2]=StudentClass.getNEUID();
         row[3]=StudentClass.getUserID();
         dtm.addRow(row);
     }
    }
