    private void btnDetailDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailDetailsActionPerformed
        // TODO add your handling code here:
        int selectedRow=jStudentDetails.getSelectedRow();
        if(selectedRow>=0){
            int dialogButton=JOptionPane.YES_NO_OPTION;
            int dialogResult=JOptionPane.showConfirmDialog(null, "Would you like to delete this row","Warning",dialogButton);
            if(dialogResult==JOptionPane.YES_OPTION){
                StudentClass studentclass=(StudentClass) jStudentDetails.getValueAt(selectedRow, 0);
                StudentDirectory.deleteStudent(studentclass);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please Select the Row","Warring",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDetailDetailsActionPerformed

    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:
        int SelectedRow=jStudentDetails.getSelectedRow();
        if(SelectedRow<0){
            JOptionPane.showMessageDialog(null,"Please select the row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            StudentClass studentclass=(StudentClass) jStudentDetails.getValueAt(SelectedRow, 0);
            
            adminStudentViewDetailsJPanel adminstudentviewdetailsjpanel=new adminStudentViewDetailsJPanel(WorkArea,StudentDirectory,studentclass);
            WorkArea.add("adminStudentViewDetailsJPanel",adminstudentviewdetailsjpanel);
            CardLayout layout=(CardLayout) WorkArea.getLayout();
            layout.next(WorkArea);
        }
    }//GEN-LAST:event_btnViewDetailsActionPerformed


